"""
Write a program that prompts the user to input a Celsius temperature and outputs
the equivalent temperature in Fahrenheit. The formula to convert the temperature is: F =
9/5 C + 32 where F is the Fahrenheit temperature and C is the Celsius temperature.
"""

f=int(input("Enter the value in fahrenheit : "))
c= (9/5)*f+32
print(" Celsius ",c)